#Escreva um programa que lê o valor em metros e o exiba convertido em centimetros e milímetros 

valor_metros = float(input('Digito valor em metros: '))
centimentos = valor_metros * 100
milimetros = valor_metros  * 1000 
print(f'O valor {valor_metros} em centimetros equivale a  {centimentos}cm e convertido para milimetros equivale a é {milimetros}mm')

